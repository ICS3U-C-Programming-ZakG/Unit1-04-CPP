// This program calculates the perimeter and area of a rectangle with dimensions of 5.7cm x 9cm.

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "5.7cm x 9cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "The area is: " << (5.7 * 9) << "cm^2" << std::endl;
    std::cout << "The perimeter is: " << (2 * (5.7 + 9)) << "cm" << std::endl;
}